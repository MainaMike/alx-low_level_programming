#include <stdio.h>
/**
 * main - A program that prints alphabets in lowercase and uppercase.
 * Return: 0 (Success)
 */
int main(void)
{
	char alpha;

	for (alpha = 'a'; alpha <= 'z'; alpha++)
	{
		putchar(char);
	}

	for (alpha = 'A'; alpha <= 'Z'; alpha++)
	{
		putchar(char);
	}

	putchar('\n');

	return (0);
}
